window.autoPlay = function () {
  function startAutoPlay(min, max, i = 0) {
    i++;
    //console.log('iteration ' + i);
    if (!window.stopAutoPlay != !(i < 30)) {

      gotoVakgebied(getRandomInt(1, 7));

      function getRandomInt(min, max) {
        return Math.floor(Math.random() * (max - min + 1)) + min;
      }

      function gotoVakgebied(vakgebied) {
        var ev = document.createEvent("SVGEvents");
        ev.initEvent("click2", true, true);

        var target = jQuery(".main-arc0").get(0); // get the SVG element here
        target.dispatchEvent(ev);

        setTimeout(function () {
          var ev = document.createEvent("SVGEvents");
          ev.initEvent("click2", true, true);

          var target = jQuery(".main-arc" + vakgebied).get(0); // get the SVG element here
          target.dispatchEvent(ev);
        }, 2000);
      }

      setTimeout(function () { startAutoPlay(1, 8, i) }, 5500);
    }
  }
  console.log('auto init ' + i);
  setTimeout(function () { startAutoPlay(1, 8) }, 5500);
}

window.stopAutoPlay = false;
